import java.util.*;
public class Bitwise
  {
  public static void main(String[] args)
    {
    int x=10;
    int y=20;
    System.out.println(x&y);
    System.out.println(x|y);
    System.out.println(x^y);
    System.out.println(~x);
    System.out.println(~y);
    System.out.println(x>>2);
    System.out.println(2<<y);
    }
  }